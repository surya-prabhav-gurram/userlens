"""
src/evaluation/llm_judge.py
LLM-as-judge evaluation: scores recommendations on 4 dimensions
using Claude as the evaluator.
"""

import json
import logging
import os
from typing import Dict, List, Optional

logger = logging.getLogger(__name__)

JUDGE_PROMPT = """You are an expert recommendation system evaluator.

User's interaction history (item titles, chronological):
{history}

Top-10 recommendations generated by the system:
{recommendations}

Score these recommendations on 4 dimensions (0-10 each):

1. Relevance (0-10): Are the recommended items genuinely relevant to this user's demonstrated tastes?
2. Diversity (0-10): Does the list cover different aspects of the user's interests, or is it repetitive?
3. Novelty (0-10): Are the recommendations surprising/exploratory vs. obvious extensions?
4. Serendipity (0-10): Does at least one recommendation feel like a delightful discovery?

Return ONLY valid JSON (no markdown):
{{"relevance": N, "diversity": N, "novelty": N, "serendipity": N, "reasoning": "brief explanation"}}"""


class LLMJudge:
    """Evaluates recommendation quality using Claude as the judge."""

    def __init__(self, api_key: Optional[str] = None, model: str = "claude-sonnet-4-20250514"):
        self.api_key = api_key or os.getenv("ANTHROPIC_API_KEY")
        self.model   = model
        self._client = None
        if self.api_key:
            try:
                import anthropic
                self._client = anthropic.Anthropic(api_key=self.api_key)
            except ImportError:
                logger.warning("anthropic not installed — LLM judge disabled")

    def score_recommendations(
        self,
        history_titles: List[str],
        rec_titles: List[str],
    ) -> dict:
        """Score a single recommendation list. Returns scores dict."""
        if not self._client:
            return {"relevance": 0, "diversity": 0, "novelty": 0, "serendipity": 0,
                    "reasoning": "LLM judge unavailable", "error": True}

        history_str = "\n".join(f"{i+1}. {t}" for i, t in enumerate(history_titles))
        recs_str    = "\n".join(f"{i+1}. {t}" for i, t in enumerate(rec_titles))
        prompt = JUDGE_PROMPT.format(history=history_str, recommendations=recs_str)

        try:
            response = self._client.messages.create(
                model=self.model,
                max_tokens=300,
                messages=[{"role": "user", "content": prompt}],
            )
            text = response.content[0].text.strip()
            if "```" in text:
                text = text.split("```")[1]
                if text.startswith("json"):
                    text = text[4:]
            return json.loads(text)
        except Exception as e:
            logger.warning(f"LLM judge failed: {e}")
            return {"relevance": 0, "diversity": 0, "novelty": 0, "serendipity": 0,
                    "reasoning": str(e), "error": True}

    def evaluate_sample(
        self,
        pipeline,
        test_targets: Dict[int, int],
        train_seqs: Dict[int, List[int]],
        id2item_titles,   # callable: model_id → title
        n_users: int = 50,
    ) -> dict:
        """Run LLM-as-judge on n_users random test users. Returns aggregate scores."""
        import random
        users = random.sample(list(test_targets.keys()), min(n_users, len(test_targets)))

        all_scores = {"relevance": [], "diversity": [], "novelty": [], "serendipity": []}
        reasonings = []

        for uid in users:
            seq = train_seqs.get(uid, [])
            result = pipeline.recommend(seq, top_k=10, mode="neural")
            rec_ids = [item["item_id"] for item in result["items"]]

            history_titles = [id2item_titles(i) for i in seq[-10:]]
            rec_titles     = [id2item_titles(i) for i in rec_ids]

            scores = self.score_recommendations(history_titles, rec_titles)
            if not scores.get("error"):
                for dim in all_scores:
                    all_scores[dim].append(scores.get(dim, 0))
                reasonings.append(scores.get("reasoning", ""))

        import numpy as np
        return {
            "n_evaluated":  len(users),
            "relevance":    float(np.mean(all_scores["relevance"])) if all_scores["relevance"] else 0,
            "diversity":    float(np.mean(all_scores["diversity"])) if all_scores["diversity"] else 0,
            "novelty":      float(np.mean(all_scores["novelty"])) if all_scores["novelty"] else 0,
            "serendipity":  float(np.mean(all_scores["serendipity"])) if all_scores["serendipity"] else 0,
            "sample_reasonings": reasonings[:3],
        }
